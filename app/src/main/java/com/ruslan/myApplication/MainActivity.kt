import androidx.compose.runtime.Composable
import androidx.compose.ui.Alignment
import androidx.compose.ui.Modifier
import androidx.compose.ui.tooling.preview.Preview
import com.ruslan.myApplication.ui.theme.MyApplicationTheme

class MainActivity : ComponentActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContent {
            MyApplicationTheme {
                // A surface container using the 'background' color from the theme
                Column(
                    modifier = Modifier.fillMaxSize(),
                    horizontalAlignment = Alignment.CenterHorizontally,
                    verticalArrangement = Arrangement.Center
                )
                {

                    Greeting("Android")
                    Button(onClick = {
                        val intent = Intent(applicationContext, Exercise1Activity::class.java)
                        startActivity(intent)
                    }) {
                       Text(text = "Homework")
                    }
                    Button(onClick = {
                        val intent = Intent(applicationContext, StartUsingCompos::class.java)
                        startActivity(intent)
                    }) {
                        Text(text = "Jetpack Compose stuff ")
                    }

                }
            }
        }
    }
}

@Composable
fun Greeting(name: String, modifier: Modifier = Modifier) {
    Text(
        text = "Hello $name!",
        modifier = modifier
    )
}

